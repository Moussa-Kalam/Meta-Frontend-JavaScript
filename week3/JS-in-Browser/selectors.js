/** JavaScript DOM Selectors
 * Use selectors to find specific objects
 * 
 * Document Object Model (DOM)
 * It is the framework a browser uses to read and store a webpage
 */

// Access the first paragraph element in your html
document.querySelector('p');

// Access the first anchor tag in your html
document.querySelector('a');

// Select all the h1 matches of the webpage
document.querySelectorAll('h1');



/* Find element with a specific id and class */

// Example id="heading"
document.getElementById('heading');

// Example class="introduction1"
document.getElementsByClassName("introduction1");

